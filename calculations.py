def convergenceperiod(p,sd_arr,d,covprob):
	if d==0:
		d=1
	if type(sd_arr)!=np.ndarray:
		Pd=((2/np.pi)**0.5*sd_arr/d)
		return convergenceperiod_single(sd_arr,d,covprob),Pd
	s=0
	for i in range(len(sd_arr)):
		k=convergenceperiod_single(sd_arr[i],d,covprob)
		s+=p[i]/max((k,1))
	Pd=((2/np.pi)**0.5*np.sum(sd_arr*p)/d)
	return int(1/s),Pd
	
	
def cutcovprobarray(Pd, covprob):
	k=getk(covprob,Pd)
	c=covprob[1:k+1]*Pd
	return k,c

def convergenceperiod_single(sd,d,covprob):
	Pd=((2/np.pi)**0.5*sd/d)
	if covprob[-1]>Pd:
		k=1+0.31826**2*(np.pi/2)*(d/sd)**2
		return int(k)
	k=getk(covprob,Pd)
	return k
	
def getk(covprob,Pd):
	k=np.nonzero(covprob<Pd)
	if len(k[0])==0:
		k=len(covprob)-1
	else:
		k=max((k[0][0]-1,0))
	return k



